package com.websarva.wings.android.navigationsamplesafejava.data.model;

public class Memo {
	private Long _id;
	private String _title;
	private String _content;

	public Long getId() {
		return _id;
	}
	public void setId(Long id) {
		_id = id;
	}
	public String getTitle() {
		return _title;
	}
	public void setTitle(String title) {
		_title = title;
	}
	public String getContent() {
		return _content;
	}
	public void setContent(String content) {
		_content = content;
	}
}
